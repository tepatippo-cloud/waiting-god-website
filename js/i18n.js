const langLabels = { th: 'ภาษาไทย (TH)', en: 'English (EN)', jp: '日本語 (JP)' };

let currentLang = localStorage.getItem('lang') || 'th';

function applyTranslations(lang) {
    const t = translations[lang];
    if (!t) return;

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key] !== undefined) {
            el.innerHTML = t[key];
        }
    });

    document.querySelectorAll('[data-i18n-attr]').forEach(el => {
        const parts = el.getAttribute('data-i18n-attr').split('|');
        const attr = parts[0];
        const key = parts[1];
        if (t[key] !== undefined) {
            el.setAttribute(attr, t[key]);
        }
    });

    // Update html lang
    document.documentElement.lang = lang === 'jp' ? 'jp' : lang === 'en' ? 'en' : 'th';

    // Update active state on lang buttons
    document.querySelectorAll('.lang-item').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });
}


function updateTrigger(lang) {
    document.querySelectorAll('.lang-dropdown').forEach(dropdown => {
        // Ensure a trigger button exists; if old globe-btn exists replace it
        let trigger = dropdown.querySelector('.lang-trigger-btn');
        const oldGlobe = dropdown.querySelector('.lang-globe-btn');
        if (!trigger) {
            trigger = document.createElement('button');
            trigger.className = 'lang-trigger-btn';
            trigger.setAttribute('aria-label', 'Select Language');
            if (oldGlobe) {
                dropdown.replaceChild(trigger, oldGlobe);
            } else {
                dropdown.insertBefore(trigger, dropdown.querySelector('.lang-menu'));
            }
            trigger.addEventListener('click', (e) => {
                e.stopPropagation();
                const wasOpen = dropdown.classList.contains('open');
                document.querySelectorAll('.lang-dropdown').forEach(d => d.classList.remove('open'));
                if (!wasOpen) dropdown.classList.add('open');
            });
        }
        trigger.innerHTML = `
            <span class="trigger-flag">${flagSVGs[lang]}</span>
            <span class="trigger-name">${langLabels[lang]}</span>
            <svg class="lang-chevron" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5">
                <path stroke-linecap="round" stroke-linejoin="round" d="M5 15l7-7 7 7"/>
            </svg>`;
    });
}

function buildLangMenu() {
    const menus = document.querySelectorAll('.lang-menu');
    menus.forEach(menu => {
        menu.innerHTML = '';
        ['th', 'en', 'jp'].forEach(lang => {
            const btn = document.createElement('button');
            btn.className = 'lang-item' + (lang === currentLang ? ' active' : '');
            btn.setAttribute('data-lang', lang);
            btn.innerHTML = `<span class="flag-icon">${flagSVGs[lang]}</span> ${langLabels[lang]}`;
            btn.addEventListener('click', () => {
                currentLang = lang;
                localStorage.setItem('lang', lang);
                applyTranslations(lang);
                updateTrigger(lang);
                document.querySelectorAll('.lang-dropdown').forEach(d => d.classList.remove('open'));
            });
            menu.appendChild(btn);
        });
    });
}

document.addEventListener('DOMContentLoaded', () => {
    buildLangMenu();
    updateTrigger(currentLang);
    applyTranslations(currentLang);

    document.addEventListener('click', () => {
        document.querySelectorAll('.lang-dropdown').forEach(d => d.classList.remove('open'));
    });
});
